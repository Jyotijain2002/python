import math


#Python has a set of built-in math functions, including an extensive math module, that allows you to perform mathematical tasks on numbers.

# min(),max() methods
print(min(12,43,45,1.56))
print(max(78,23.3,89,56))


# abs() method
print(abs(-23.67))

#pow() method
print(pow(2,9))

#math module methods 

print(math.sqrt(45))

#ceil() and floor() method
print(math.ceil(5.67))
print(math.ceil(5.1))
print(math.floor(5.1))
print(math.floor(5.67))

print(math.pi)